package Animales;



public abstract class Animal  {
    protected String sonido; 
    protected String alimentos; 
    protected String habitat; 
    protected String nombreCientifico;
    public abstract java.lang.String getNombreCientifico();
    public abstract java.lang.String getSonido();
    public abstract java.lang.String getAlimentos();
    public abstract java.lang.String getHabitat();

    
}


